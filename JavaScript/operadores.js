// Suma 

console.log( 5 + 4 );

// Resta 

console.log ( 10 - 6 );

// Multiplicacion 

console.log ( 3 * 4 );

// Division 

console.log ( 10 / 2 );

// Modulo 

console.log ( 10 % 2 );

// Incremento

let numero = 6 ;

console.log ( ++numero );

// Decremento 

let otroNumero = 10 ;

console.log ( --otroNumero );

// Comparacion simple

console.log( 5 == '5');

// Comparacion estricta

console.log( 5 === '5');

// Desigualdad simple

console.log( 5 != '5');

// Desigualdad estricta 

console.log( 5 !== '5');

// Mayor que

console.log( 6 > 6);

// Mayor o Igual 

console.log( 6 >= 6);

// Menor que 

console.log( 7 < 7 );

// Menor o Igual que

console.log( 7 <= 7);

// AND &&

console.log( true && true);

// OR ||

console.log( true || true);

// Contatenacion

let nombre = 'Nacho'
let apellido = 'Vuotto'

console.log( nombre + ' ' + apellido );
